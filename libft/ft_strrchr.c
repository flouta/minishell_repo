#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	last;

	i = 0;
	last = -1;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			last = i;
		i++;
	}
	if (last != -1)
		return ((char *)&s[last]);
	if (c == 0)
		return ((char *)(s + ft_strlen(s)));
	return (NULL);
}
