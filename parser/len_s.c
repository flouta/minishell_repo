#include "../Includes/minishell.h"

int	len_s(char **s)
{
	int	len;
	int	i;

	if(!s)
		return 0;
	len = 0;
	i = 0;
	while (s[i] != 0)
	{
		len++;
		i++;
	}
	return (len);
}
